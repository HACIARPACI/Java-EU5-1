package week2Muhtar;

public class ifStatements3 {

	public static void main(String[] args) {
		int day =7;
		String name ="";
		if(day>=1 && day<=7) {
			if(day==1) {
				name ="monday";
			}else if(day==2){
				name="tuesday";
			}else if(day==3) {
				name ="wednesday";
			}else if(day==4) {
				name="thursday";
			}else if(day==6) {
				name="friday";
			}else {
				name ="invalid day";
			}
		} System.out.println(name);
		
		// bunun yerine ternary kullanılablir
		
		
		
		
		
		
		
	}

}
