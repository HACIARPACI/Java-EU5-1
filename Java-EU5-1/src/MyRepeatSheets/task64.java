package MyRepeatSheets;

public class task64 {

	public static void main(String[] args) {

		starPattern(8);
		
	}
public static void starPattern(int k) {
	
	for (int i = 0; i <k; i++) {
		for (int j = 0; j <=i; j++) {
			System.out.print("* ");
		}
		System.out.println("");
	}
	
	
	
}
	
}
